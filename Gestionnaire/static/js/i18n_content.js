/* ================================================================
   DOM TRANSLATION ENGINE
================================================================ */

function translateDOM(lang) {
  if (lang === 'fr' || !CONTENT_TRANSLATIONS[lang]) return;
  const t = CONTENT_TRANSLATIONS[lang];

  function translateTextNode(node) {
    const raw = node.textContent;
    const trimmed = raw.trim();
    if (!trimmed) return;

    // 1. Exact match
    if (t.exact[trimmed]) {
      node.textContent = raw.replace(trimmed, t.exact[trimmed]);
      return;
    }

    // 2. Starts-with match (dynamic suffix)
    for (const [frPrefix, targetPrefix] of t.startsWith) {
      if (trimmed.startsWith(frPrefix)) {
        const suffix = trimmed.slice(frPrefix.length);
        node.textContent = raw.replace(trimmed, targetPrefix + suffix);
        return;
      }
    }
  }

  function walk(node) {
    if (node.nodeType === Node.TEXT_NODE) {
      translateTextNode(node);
    } else if (node.nodeType === Node.ELEMENT_NODE) {
      const tag = node.tagName;
      if (tag === 'SCRIPT' || tag === 'STYLE' || tag === 'NOSCRIPT') return;

      // Translate placeholder attribute
      if (node.placeholder) {
        const p = node.placeholder.trim();
        if (t.exact[p]) node.placeholder = t.exact[p];
      }

      // Translate title attribute (tooltips)
      if (node.title) {
        const ti = node.title.trim();
        if (t.exact[ti]) node.title = t.exact[ti];
      }

      node.childNodes.forEach(walk);
    }
  }

  walk(document.body);
}

// Run after full DOM load
document.addEventListener('DOMContentLoaded', function () {
  const lang = document.documentElement.lang || 'fr';
  translateDOM(lang);
});
